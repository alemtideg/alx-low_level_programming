/**
 * _memcpy - prints memory area
 * @dest: memory area
 * @src: memory area
 * @n: number of bytes
 *
 * Return: return pointers to dest
 */
char *_memcpy(char *dest, char *src, unsigned int n)
{
	unsigned int i;

	for (i = 0; i < 0; i++)
	{
		dest[i] = src[i];
	}
	return (dest);
}
